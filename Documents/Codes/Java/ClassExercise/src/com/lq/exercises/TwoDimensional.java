package com.lq.exercises;

public interface TwoDimensional {

    /**
     * Método para calcular a área de uma forma bidimensional.
     * 
     * @return a área da forma bidimensional
     */
    public double getArea();

    /**
     * Método para calcular o perímetro de uma forma bidimensional.
     * 
     * @return o perímetro da forma bidimensional
     */
    public double getPerimetro();
}
